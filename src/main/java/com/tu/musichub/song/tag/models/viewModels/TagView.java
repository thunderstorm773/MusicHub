package com.tu.musichub.song.tag.models.viewModels;

public class TagView {

    private Long id;

    private String name;

    public TagView() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
